package temalab11;

public class Calculator {
    public static float add(int x, int y) {
        return x + y;
    }

    public static float divide(float x, float y) {

        return x / y;
    }

    public static float multiply(float x, float y) {
        return x * y;
    }

    public static float subtract(float x, float y) {
        return x - y;
    }
}
